let lang = 'en';

function setLang(l) {
  lang = l;
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (T[lang][key] !== undefined) el.textContent = T[lang][key];
  });
  document.querySelectorAll('[data-i18n-ph]').forEach(el => {
    const key = el.getAttribute('data-i18n-ph');
    if (T[lang][key] !== undefined) el.placeholder = T[lang][key];
  });
  document.documentElement.lang = l;
  document.querySelectorAll('.lang-toggle button').forEach(btn => {
    btn.classList.toggle('active', btn.dataset.lang === l);
  });
  // Persist
  try { localStorage.setItem('dlm-lang', l); } catch(e) {}
}

function t(key) {
  return T[lang][key] || key;
}

function initLang() {
  let saved;
  try { saved = localStorage.getItem('dlm-lang'); } catch(e) {}
  const browser = navigator.language?.startsWith('pt') ? 'pt' : 'en';
  setLang(saved || browser);
}
